package com.spduniversity.javacore.hw3.homecinema;

public class PopcornPopper {

    void on() {
        System.out.println("Включить аппарат для попкорна.");

    }

    void off() {
        System.out.println("Выключить аппарат для попкорна");
    }

    void pop() {
        System.out.println("Запустить приготовление попкорна.");
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
